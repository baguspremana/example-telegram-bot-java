package model;

public class LastMenu {
    int id;
    Long chat_id;
    String last_menu;

    public LastMenu() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Long getChat_id() {
        return chat_id;
    }

    public void setChat_id(Long chat_id) {
        this.chat_id = chat_id;
    }

    public String getLast_menu() {
        return last_menu;
    }

    public void setLast_menu(String last_menu) {
        this.last_menu = last_menu;
    }
}
